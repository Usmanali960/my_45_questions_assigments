"use strict";
let car = "subharu";
// True cases
console.log(car === "subharu");
console.log(car !== "civic");
console.log(car.endsWith("u"));
console.log(car.startsWith("s"));
console.log(car == "subharu");
// False cases
console.log(car !== "subharu");
console.log(car === "civic");
console.log(car.endsWith("c"));
console.log(car.startsWith("u"));
console.log(car != "subharu");
