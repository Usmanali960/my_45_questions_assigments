// Equality test or Inequality tset with strings
console.log("apple" === "apple");
console.log(("apple" as string) != "apple");

// lower case function
console.log("USMAN".toLowerCase() === "usman");

//greater than or less than
console.log(5 > 2);
console.log(5 < 2);

//greater than or equal to less than or equal to
console.log(5 >= 2);
console.log(5 <= 2);

// test with and operator and test with or operator
console.log(5 === 5 && 10 === 10);
console.log(5 === 5 || true);

// test item is in array or test item is not in array
let arr:string[] = ["apple"];
arr.includes("apple");
arr.includes("orange");

